#include <iostream>
using namespace std;

int main() {
    int age = 21;           // Integer variable
    float height = 5.9;     // Floating-point variable
    char grade = 'A';       // Character variable
    bool passed = true;     // Boolean variable

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;
    cout << "Passed: " << passed << endl;

    return 0;
}
